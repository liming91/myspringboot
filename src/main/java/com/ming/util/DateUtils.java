    /**
     * 计算两个时间段时间差，精确到秒
     *
     * @param startTime 2019-04-10 17:16:11
     * @param endTime   2019-04-10 17:28:17
     * @return
     */
    public static String computationTime(String startTime, String endTime) {
        String str = null;
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            Date endDate = sdf.parse(endTime);
            Date strDate = sdf.parse(startTime);
            long diff = endDate.getTime() - strDate.getTime();
            long day = diff / nd;
            long hour = diff % nd / nh;
            long min = diff % nd % nh / nm;
            long sec = diff % nd % nh % nm / 1000;//秒
            if (day == 0) {
                str = hour + "小时" + min + "分钟";
            } else if (hour == 0) {
                str = min + "分钟";
            } else {
                str = day + "天" + hour + "小时" + min + "分钟";
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return str;
    }

    public static String minTime(long minute) {
        String str = null;
        long days = minute / 1440;
        long hours = minute % 1440 / 60;
        long minutes = minute % 60;

        if (days == 0) {
            str = hours + "小时" + minutes + "分钟";
        } else if (hours == 0) {
            str = minutes + "分钟";
        } else {
            str = days + "天" + hours + "小时" + minutes + "分钟";
        }
        return str;
    }


